document.addEventListener('DOMContentLoaded', function() {
    // Handle Consultation Form submission on English page
    const consultationForm = document.getElementById('consultationFormEn');
    const nameInput = document.getElementById('consultationNameEn');
    const emailInput = document.getElementById('consultationEmailEn');
    const brevoConsent = document.getElementById('brevoConsentEn');
    const submitBtn = consultationForm.querySelector('button[type="submit"]');
    
        if (consultationForm) {
            // Store original form HTML for reset
            const originalFormHTML = consultationForm.innerHTML;
            
            // Reset form when modal is shown
            $('#consultationModal').on('show.bs.modal', function () {
                consultationForm.innerHTML = originalFormHTML;
                // Re-get elements after reset
                const newNameInput = document.getElementById('consultationNameEn');
                const newEmailInput = document.getElementById('consultationEmailEn');
                const newBrevoConsent = document.getElementById('brevoConsentEn');
                const newSubmitBtn = consultationForm.querySelector('button[type="submit"]');
                
                // Re-bind submit event
                if (newSubmitBtn) {
                    newSubmitBtn.addEventListener('click', handleFormSubmit);
                }
                
                function handleFormSubmit(e) {
                    e.preventDefault();
                    
                    const name = newNameInput.value.trim();
                    const email = newEmailInput.value.trim();
                    const consent = newBrevoConsent.checked;
            
            // Validation
            if (!name || !email) {
                alert('Please fill in all information!');
                return;
            }
            
            const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
            if (!emailRegex.test(email)) {
                alert('Please enter a valid email!');
                return;
            }
            
                    // Disable form and show loading
                    newSubmitBtn.disabled = true;
                    newSubmitBtn.innerHTML = 'PROCESSING...';
                    
                    // Send AJAX request
                        fetch('<?php echo admin_url("admin-ajax.php"); ?>', {
                            method: 'POST',
                            headers: {
                                'Content-Type': 'application/x-www-form-urlencoded',
                            },
                            body: new URLSearchParams({
                                action: 'consultation_signup',
                                name: name,
                                email: email,
                                brevo_consent: consent ? '1' : '0',
                                nonce: '<?php echo wp_create_nonce("consultation_signup_nonce"); ?>',
                                source: 'page_modal_en'
                            })
                        })
                        .then(response => {
                            console.log('Response status:', response.status);
                            return response.text().then(text => {
                                console.log('Raw response:', text);
                                try {
                                    return JSON.parse(text);
                                } catch (e) {
                                    console.error('JSON parse error:', e);
                                    throw new Error('Invalid JSON response');
                                }
                            });
                        })
            .then(data => {
                if (data.success) {
                    // Show success message in form
                    const formContent = consultationForm.innerHTML;
                    consultationForm.innerHTML = `
                        <div class="text-center py-4">
                            <div style="color: #28a745; font-size: 18px; font-weight: 600; margin-bottom: 10px;">
                                <i class="fas fa-check-circle" style="font-size: 24px; margin-right: 8px;"></i>
                                Registration Successful!
                            </div>
                            <p style="color: #666; font-size: 14px; margin: 0;">
                                We will contact you soon.
                            </p>
                        </div>
                    `;
                    
                    // Close modal after 2 seconds
                    setTimeout(() => {
                        $('#consultationModal').modal('hide');
                        // Alternative method
                        setTimeout(() => {
                            $('#consultationModal').modal('hide');
                        }, 100);
                        // Force close if jQuery modal doesn't work
                        setTimeout(() => {
                            const modal = document.getElementById('consultationModal');
                            if (modal) {
                                modal.classList.remove('show');
                                modal.style.display = 'none';
                                document.body.classList.remove('modal-open');
                                const backdrop = document.querySelector('.modal-backdrop');
                                if (backdrop) backdrop.remove();
                            }
                        }, 200);
                    }, 2000);
                } else {
                    alert('Error occurred: ' + data.data);
                }
            })
            .catch(error => {
                console.error('AJAX error:', error);
                alert('Error occurred. Please try again later.');
            })
                    .finally(() => {
                        // Re-enable form
                        newSubmitBtn.disabled = false;
                        newSubmitBtn.innerHTML = 'REGISTER FOR CONSULTATION';
                    });
                }
            });
        }
    });
});
